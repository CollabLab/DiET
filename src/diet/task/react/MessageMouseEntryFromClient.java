package diet.task.react;

import diet.message.MessageTask;

/**
 *
 * @author sre
 */
public class MessageMouseEntryFromClient extends MessageTask{

    String buttonname;
    int x;
    int y;
    long time;

    public String getButtonname() {
        return buttonname;
    }

    public long getTime() {
        return time;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }



    public MessageMouseEntryFromClient(String email, String username, String buttonname, int x, int y, long time) {
        super(email,username);
        this.buttonname=buttonname;
        this.x=x;
        this.y=y;
        this.time=time;
    }



}
